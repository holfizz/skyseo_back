#!/bin/bash

# Скрипт для полной очистки и пересборки бэкенда

echo "🧹 Полная очистка и пересборка бэкенда..."

# Останавливаем и удаляем контейнеры
echo "1️⃣ Останавливаем контейнеры..."
docker-compose down

# Удаляем образ backend
echo "2️⃣ Удаляем старый образ..."
docker rmi skyseo_back-backend 2>/dev/null || echo "Образ не найден, пропускаем"

# Удаляем dist локально (если есть)
echo "3️⃣ Очищаем локальную папку dist..."
rm -rf dist

# Пересобираем образ с нуля
echo "4️⃣ Пересобираем образ (это может занять время)..."
docker-compose build --no-cache backend

# Запускаем контейнеры
echo "5️⃣ Запускаем контейнеры..."
docker-compose up -d

# Ждем 10 секунд для запуска
echo ""
echo "⏳ Ожидание запуска (10 сек)..."
sleep 10

# Показываем логи
echo ""
echo "📋 Логи бэкенда:"
docker-compose logs -f backend
